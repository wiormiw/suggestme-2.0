MOOD_LIST.forEach((mood: Mood) => {
  const foodNames = FOOD_DATA[mood];
  foodNames.forEach((name) => {
    if (foodsToSeed.length <= 1000) {
      foodsToSeed.push({
        id: Bun.randomUUIDv7(),
        name: name.trim(),
        mood: mood,
        isAvailable: true,
      });
      foodId++;
    }
  });
});

